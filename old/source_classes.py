#!usr/bin/env python
"""sample classes"""

class alpha():
    def func(self, x, y):
        return 2 * x - y
    x = 3
    y = 4

class beta():
    def func(self):
        x = 3
        y = 4
        return 2 * x - y

class gamma():
    def func(self):
        return 2 * 3 - 4

class delta():
    def func(self):
        return 2
